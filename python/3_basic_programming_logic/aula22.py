'''
Conversão de tipo,coerção (type convertion, typecasting, coercion)
é o ato de convertem um tipo em outro

#tipos imútaveis e primitivos:
    str, int, float, bool 
'''

print(int('1'), type(int('1')), sep=' -> ')
print(float('1') + 1, type(float('1')+1), sep=' -> ')
print(str(1) + 'b', type(str(1) + 'b'), sep=' -> ')
print(bool('')) #Uma string vazia é considerada False
