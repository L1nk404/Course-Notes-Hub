'''
Retorno de valores das funções (return)
'''

# A função por padrão tem o valor de none.
def soma(x, y):
    '''
    Para atribuir um valor na função, usamos 'return', assim, podemos
    atribuí-la em uma variável e ela retornará o valor que pedimos.
    '''
    return x + y

soma1 = soma(2, 2)
soma2 = soma(3, 3)

print(soma1 + soma2)