-- SELECT - Recovering Data

-- This commands is made to consult the tables.

-- Syntax:
-- SELECT   [DISTINCT] (*| column [alias], ...)
-- FROM     table
-- [WHERE   condition(s)];

-- SELECT       specifies a list of one or more columns
-- DISTINCT     suppresses duplicate rows
-- *            selects all columns
-- <column>       selects the named column
-- alias        provides different headers for the selected columns
-- FROM <table>   specifies the table containing the columns